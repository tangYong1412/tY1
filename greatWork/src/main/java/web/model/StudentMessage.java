package main.java.web.model;

public class StudentMessage {
    String userNo;
    String userNa;
    String uerAge;
    String userSex;
    String uerPs;
    String cname;
    String grade;

    public String getUserNo() {
        return userNo;
    }

    public void setUserNo(String userNo) {
        this.userNo = userNo;
    }

    public String getUserNa() {
        return userNa;
    }

    public void setUserNa(String userNa) {
        this.userNa = userNa;
    }

    public String getUerAge() {
        return uerAge;
    }

    public void setUerAge(String uerAge) {
        this.uerAge = uerAge;
    }

    public String getUserSex() {
        return userSex;
    }

    public void setUserSex(String userSex) {
        this.userSex = userSex;
    }

    public String getUerPs() {
        return uerPs;
    }

    public void setUerPs(String uerPs) {
        this.uerPs = uerPs;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }
}
